
package com.sikefeng.chinaren.entity.model;

public class BaseData {

    /**
     * 状态
     */
    private int status;
    /**
     * 消息
     */
    private String msg;

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
